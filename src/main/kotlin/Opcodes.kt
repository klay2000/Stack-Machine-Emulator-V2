enum class Opcodes(val code: Int) {
    //2 arg
    ADD(0x01),
    SUB(0x02),
    MUL(0x03),
    DIV(0x04),
    SHL(0x05),
    SHR(0x06),
    AND(0x07),
    OR(0x08),
    XOR(0x09),
    MOV(0x0A),
    SAV(0x0B),
    //1 arg
    LD(0x10),
    NOT(0x11),
    INC(0x12),
    DEC(0x13),
    JMP(0x14),
    JLT(0x15),
    JMT(0x16),
    JEQ(0x17),
    //no arg
    TRU(0x20),
    FAL(0x21),
    PSH(0x22),
    POP(0x23),
    IMM(0x24),
    JIM(0x25),
    NOP(0x00)
}